public class Computer {
    int screenSize;
    int memory;

    public Computer() {
        screenSize = 0;
        memory = 0;
    }

    public int getScreenSize() {
        return screenSize;
    }

    public int getMemory() {
        return memory;
    }

    public void setScreenSize(int screenSize) {
        this.screenSize = screenSize;
    }

    public void setMemory(int memory) {
        this.memory = memory;
    }
}
